SELECT
    TCMCS023.T$CITG       NUM_GRUPO_ITEM,
    TCMCS023.T$DSCA       NOM_GRUPO_ITEM,
    ZNMCS030.T$SETO$C     NUM_SETOR,
    ZNMCS030.T$DSCA$C     NOM_SETOR,
    ZNMCS031.T$FAMI$C     NUM_FAMILIA,
    ZNMCS031.T$DSCA$C     NOM_FAMILIA,
    ZNMCS032.T$SUBF$C     NUM_SUBFAMILIA,
    ZNMCS032.T$DSCA$C     NOM_SUBFAMILIA
FROM
    BAANDB.TTCMCS023301               TCMCS023
    LEFT JOIN   BAANDB.TZNMCS030301   ZNMCS030  ON  ZNMCS030.T$CITG$C = TCMCS023.T$CITG
    LEFT JOIN   BAANDB.TZNMCS031301   ZNMCS031  ON  ZNMCS031.T$CITG$C = TCMCS023.T$CITG
                                                AND ZNMCS031.T$SETO$C = ZNMCS030.T$SETO$C
    LEFT JOIN   BAANDB.TZNMCS032301   ZNMCS032  ON  ZNMCS032.T$CITG$C = TCMCS023.T$CITG
                                                AND ZNMCS032.T$SETO$C = ZNMCS030.T$SETO$C
                                                AND ZNMCS032.T$FAMI$C = ZNMCS031.T$FAMI$C