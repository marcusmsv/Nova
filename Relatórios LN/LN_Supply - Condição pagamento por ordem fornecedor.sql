		ZNPUR008.T$FOVN$C				CNPJ_CPF,
		TCCOM938.T$DESA$L				RAZAO_SOCIAL,
		ZNPUR008.T$POTP$C				COD_TIPO_ORDEM,
		TDPUR094.T$DSCA					DESCR_TIPO_ORDEM,
		ZNPUR008.T$CPAY$C				COD_COND_PGTO,
		TCMCS013.T$DSCA					DESCR_COND_PGTO
		
