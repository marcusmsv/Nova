SELECT
		TDIPU010.T$CITG			ID_GRUPO_ITEM,
		TCMCS023.T$DSCA			DESCR_GRUPO_ITEM,
		TCCOM130.T$FOVN$L		CNPJ_FORNECEDOR,
		TCCOM966.T$DESA$D		RAZAO_SOCIAL,
		TDIPU010.T$VLMF$C		VALOR_MINIMO,
		TDIPU010.T$MAIL$C		EMAIL_OC
FROM
			BAANDB.TTDIPU010301	TDIPU010
INNER JOIN	BAANDB.TTCMCS023301	TCMCS023	ON	TCMCS023.T$CITG		=	TDIPU010.T$CITG
INNER JOIN	BAANDB.TTCCOM100301	TCCOM100	ON	TCCOM100.T$BPID		=	TDIPU010.T$OTBP
INNER JOIN	BAANDB.TTCCOM130301	TCCOM130	ON	TCCOM130.T$CADR		=	TCCOM100.T$CADR
INNER JOIN	BAANDB.TTCCOM966301	TCCOM966	ON	TCCOM966.T$FTYP$L	=	TCCOM130.T$FTYP$L
											AND	TCCOM966.T$FOVN$L	=	TCCOM130.T$FOVN$L

WHERE
		TDIPU010.T$EFDT = (	SELECT MAX(A.T$EFDT)
							FROM BAANDB.TTDIPU010301 A
							WHERE A.T$CITG=TDIPU010.T$CITG
							AND A.T$ITEM=TDIPU010.T$ITEM
							AND A.T$OTBP=TDIPU010.T$OTBP
							AND A.T$SFBP=TDIPU010.T$SFBP)