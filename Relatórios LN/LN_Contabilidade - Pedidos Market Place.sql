SELECT 
		CISLI940.T$DATE$L			DATA_EMISSAO,
		CISLI940.T$FIRE$L			REF_FISCAL,
		CISLI940.T$DOCN$L			NF,
		CISLI940.T$SERI$L			SERIE_NF,
		CISLI940.T$FDTC$L			COD_TIPO_DOC_FISCAL,
		TCMCS966.T$DSCA$L			DESCR_TIPO_DOC_FISCAL,
		CISLI940.T$ITYP$L			TIPO_TRANSACAO,
		CISLI940.T$IDOC$L			NUM_DOCUMENTO,
		ZNSLS401.T$PECL$C			NUM_PEDIDO,
		ZNSLS401.T$SQPD$C			SEQ_PEDIDO,
		ZNSLS401.T$IDLO$C			COD_LOGISTA,
		CISLI940.T$BPID$L			ID_PARCEIRO,				-- CLIENTE
		TCCOM130.T$FOVN$L			CPF_CNJ_CLIENTE,
		SUM(CISLI940.T$GAMT$L)		VALOR_ITEM_NF,
		SUM(CISLI940.T$FGHT$L)		VALOR_FRETE_NF,
		SUM(CISLI940.T$AMNT$L)		VALOR_TOTAL_NF	
FROM  BAANDB.TCISLI940201 CISLI940

INNER JOIN	BAANDB.TCISLI941201 CISLI941
				ON	CISLI941.T$FIRE$L	=	CISLI940.T$FIRE$L
				
INNER JOIN	BAANDB.TTCIBD001201 TCIBD001
				ON	TCIBD001.T$ITEM = CISLI941.T$ITEM$L
				
INNER JOIN	BAANDB.TTCCOM130201 TCCOM130
				ON	TCCOM130.T$CADR		=	CISLI940.T$ITOA$L
				
INNER JOIN	BAANDB.TCISLI245201 CISLI245
				ON	CISLI245.T$FIRE$L	=	CISLI941.T$FIRE$L
				AND	CISLI245.T$LINE$L	=	CISLI941.T$LINE$L
				
INNER JOIN	BAANDB.TZNSLS004201 ZNSLS004
				ON	ZNSLS004.T$ORNO$C	=	CISLI245.T$SLSO
				AND	ZNSLS004.T$PONO$C	=	CISLI245.T$PONO
				
LEFT JOIN	BAANDB.TZNSLS401201 ZNSLS401
				ON	ZNSLS401.T$NCIA$C	=	ZNSLS004.T$NCIA$C
				AND ZNSLS401.T$UNEG$C	=	ZNSLS004.T$UNEG$C
				AND ZNSLS401.T$PECL$C	=	ZNSLS004.T$PECL$C
				AND ZNSLS401.T$SQPD$C	=	ZNSLS004.T$SQPD$C
				AND ZNSLS401.T$ENTR$C	=	ZNSLS004.T$ENTR$C
				AND ZNSLS401.T$SEQU$C	=	ZNSLS004.T$SEQU$C

INNER JOIN	BAANDB.TTCMCS966201	TCMCS966
				ON	TCMCS966.T$FDTC$L	=	CISLI940.T$FDTC$L

WHERE		TCIBD001.T$CITG = '1000' 

GROUP BY
			CISLI940.T$DATE$L,
            CISLI940.T$FIRE$L,
            CISLI940.T$DOCN$L,
            CISLI940.T$SERI$L,
            CISLI940.T$FDTC$L,
            TCMCS966.T$DSCA$L,
            CISLI940.T$ITYP$L,
            CISLI940.T$IDOC$L,
            ZNSLS401.T$PECL$C,
            ZNSLS401.T$SQPD$C,
            ZNSLS401.T$IDLO$C,
            CISLI940.T$BPID$L,
            TCCOM130.T$FOVN$L